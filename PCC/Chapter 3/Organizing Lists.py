# Sort a list permanently with sort() method
# no argument will auto sort alphabetically for strings
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)
# Sort in reverse alphabetical order
cars.sort(reverse=True)
print(cars)
# Sort a list temporarily with sorted() function
print("Here is the orginal list.")
print(cars)

print("\nHere is the sorted list:")
print(sorted(cars))

print("\nHere is the original list again:")
print(cars)

# Printing a list in reverse order
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(cars)

cars.reverse()
print(cars)

# Find the length of a list using len() function
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(len(cars))



